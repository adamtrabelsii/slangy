const DICTS: Record<LangCode, Dict> = { en, fr, es, ar };

export function translate(lang: LangCode, key: string, params?: Record<string, string | number>): string {
  let s = DICTS[lang]?.[key] ?? en[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) s = s.replace(`{${k}}`, String(v));
  }
  return s;
}

export type TFn = (key: string, params?: Record<string, string | number>) => string;

/** Hook: returns a translator bound to the learner's native language. */
export function useT(): TFn {
  const from = useStore((s) => s.learnFrom);
  return (key, params) => translate(from, key, params);
}
